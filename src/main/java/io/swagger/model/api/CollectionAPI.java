package io.swagger.model.api;

import java.math.BigDecimal;

public class CollectionAPI {
	BigDecimal documents;
	BigDecimal parts;
	BigDecimal similarities;
	BigDecimal subdomains;
	BigDecimal topics;
	
	public BigDecimal getDocuments() {
		return documents;
	}
	public void setDocuments(BigDecimal documents) {
		this.documents = documents;
	}
	public BigDecimal getParts() {
		return parts;
	}
	public void setParts(BigDecimal parts) {
		this.parts = parts;
	}
	public BigDecimal getSimilarities() {
		return similarities;
	}
	public void setSimilarities(BigDecimal similarities) {
		this.similarities = similarities;
	}
	public BigDecimal getSubdomains() {
		return subdomains;
	}
	public void setSubdomains(BigDecimal subdomains) {
		this.subdomains = subdomains;
	}
	public BigDecimal getTopics() {
		return topics;
	}
	public void setTopics(BigDecimal topics) {
		this.topics = topics;
	}

	
	
}

  